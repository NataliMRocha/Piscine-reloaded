#include <unistd.h>

void	ft_putchar(char c);

void	ft_print_params(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		ft_putchar(str[c]);
		c++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_print_params(argv[i]);
		ft_putchar('\n');
		i++;
	}
}
